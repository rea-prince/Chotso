/* Render Image */

void
Chip8_render(Chip8* chip8, SDL_Texture* tex) {

    void* pixels;
    int   pitch;

    uint32_t* targetPixel;
    uint32_t  color;
    uint8_t   pix;

    int row;
    int col;

    SDL_LockTexture(tex, NULL, &pixels, &pitch);

    for (int y = 0; y < CHIP8_HEIGHT; y++) {

        row = y * CHIP8_WIDTH / 8;

        for (int x = 0; x < CHIP8_WIDTH; x++) {

            targetPixel = (uint32_t*) ((uint8_t*) pixels + (y * pitch) + (x * sizeof(uint32_t)));

            col = row + (x / 8);

            pix = (chip8->display[col] >> (7 - (x % 8))) & 0x1;

            color = pix ? 0xFFFFFFFF : 0x000000FF;

            *targetPixel = color;
        }
    }

    SDL_UnlockTexture(tex);
}

/* FETCH INSTRUCTION */

uint16_t
Chip8_fetch(Chip8* chip8) {
    uint16_t instruction = 0x0;

    instruction += (chip8->memory[chip8->PC++] << 8);
    instruction += chip8->memory[chip8->PC];

    return instruction;
}

