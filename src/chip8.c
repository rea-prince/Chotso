/* Render Image */

void
Chip8_render(Chip8 chip8, SDL_Texture* tex) {

    void* pixels;
    int   pitch;

    uint32_t* targetPixel;
    uint32_t  color;
    uint8_t   pix;

    SDL_LockTexture(tex, NULL, &pixels, &pitch);

    for (int y = 0; y < CHIP8_HEIGHT; y++) {
        for (int x = 0; x < CHIP8_WIDTH; x++) {

            targetPixel = (uint32_t*) (pixels + (y * pitch) + (x * sizeof(uint32_t)));

            pix = (chip8.display[y][x / 8] >> (7 - (x % 8))) & 0x1;

            color = pix ? 0xFFFFFFFF : 0x000000FF;

            *targetPixel = color;
        }
    }

    SDL_UnlockTexture(tex);
}

/* FETCH INSTRUCTION */

uint16_t
Chip8_fetch(Chip8* chip8) {
    uint16_t instruction = 0x0;
    uint8_t PC = chip8->PC;

    instruction += (chip8->memory[PC++] << 8);
    instruction += chip8->memory[PC];

    chip8->PC = PC;

    return instruction;
}

